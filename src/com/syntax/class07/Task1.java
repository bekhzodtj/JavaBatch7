package com.syntax.class07;

public class Task1 {

	public static void main(String[] args) {
		//how to print 50 to 1
		//how to print odds 1 to 20
		int a = 50;
		while (a >= 1) {
		System.out.println(a);
		a--;
		}
		System.out.println("***************************");
		int b = 1;
		while (b <= 20) {
		System.out.println(b);
		b += 2;
		}
		System.out.println("=============================");

	}

}
